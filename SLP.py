def SLP(a):
    
    a.sort()
    c = { int(b): "" for b in a}

    for b in a:
        c[int(b)] += f"{int(round(b - int(b), 1) * 10)}"
        
    print(" Stem | Leaf\n")
    
    for b in c:
        print(f"  {b}  |  {int(c[b])}")
        
        
